const dicts: Record<string, Record<string, string>> = { EN: en, HR: hr };

const LanguageContext = createContext<LanguageContextValue>({
  lang: 'HR',
  setLang: () => {},
  t: (key) => key,
});

export const LanguageProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [lang, setLangState] = useState<LangCode>(() => {
    const stored = localStorage.getItem('cenner_lang');
    if (stored) return stored;
    // Auto-detect from browser language — default HR only for Croatian browsers
    const browserLang = navigator.language?.split('-')[0]?.toUpperCase();
    return (browserLang && dicts[browserLang]) ? browserLang : 'EN';
  });

  const setLang = (code: LangCode) => {
    setLangState(code);
    localStorage.setItem('cenner_lang', code);
  };

  const t = (key: string): string => {
    const dict = dicts[lang] || dicts['EN'];
    if (dict && dict[key] !== undefined) return dict[key];
    // Fallback to English
    if (dicts['EN'] && dicts['EN'][key] !== undefined) return dicts['EN'][key];
    // Fallback to key itself
    return key;
  };

  return (
    <LanguageContext.Provider value={{ lang, setLang, t }}>
      {children}
    </LanguageContext.Provider>
  );
};

export const useLanguage = () => useContext(LanguageContext);
export const useT = () => useContext(LanguageContext).t;
